dp = [0]*11

dp[1] = 1 # 1
dp[2] = 2 # 1+1, 2
dp[3] = 4 # 1+1+1, 1+2, 2+1, 3
dp[4] = 7 # 1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2, 1+3, 3+1
dp[5] = 13 #1+1+1+1+1, 1+1+1+2 곱하기 4, 1+1+3 곱하기 3, 1+2+2, 2+1+2, 2+2+1, 2 + 3, 3 + 2

for i in range(6, 11):
    dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

t = int(input())
for _ in range(t):
    print(dp[int(input())])



