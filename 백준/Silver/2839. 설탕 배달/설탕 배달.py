N = int(input())

dp = [-1] * 5001
dp[3] = 1
dp[5] = 1
dp[6] = 2
dp[8] = 2

for i in range(9, len(dp)):
    if i == 4 or i == 7:
        dp[i] = -1
    elif i % 5 == 0:
        dp[i] = dp[5]*(i // 5)
    elif i % 5 == 1 or i % 5 == 3:
        dp[i] = dp[(i//5)*5] + 1
    elif i % 5 == 2 or i % 5 == 4:
        dp[i] = dp[(i//5)*5] + 2

print(dp[N])